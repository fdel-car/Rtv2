#include "rt.h"

int		count_esize(char **t)
{
	int		i;
	int		na;

	i = 0;
	na = 0;
	if (ft_sii(t[i], "{"))
		while (t[i] && (na > 0 || !i))
		{
			ft_sii(t[i], "{") ? ++na : 0;
			ft_sii(t[i], "}") ? --na : 0;
			++i;
		}
	return (i);
}
